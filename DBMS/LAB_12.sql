--1. Produce Output Like: <PersonName> earns <Salary> from <DepartmentName> department monthly. (In 
--single column)
SELECT P.PERSONNAME+' EARNS '+STR(P.SALARY)+' FROM ' + D.DEPARTMENTNAME+' DEPARTMENT MONTHLY ' FROM PERSON P
LEFT JOIN DEPT D
ON P.DEPARTMENTID = D.DEPARTMENTID 

--2. Find city & department wise total, average & maximum salaries.
SELECT P.CITY,D.DEPARTMENTNAME,SUM(P.SALARY),AVG(P.SALARY),MAX(P.SALARY) FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY P.CITY,D.DEPARTMENTNAME

--3. Find all persons who do not belong to any department.
SELECT P.PERSONNAME FROM PERSON P
LEFT JOIN DEPT D
ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE D.DEPARTMENTID IS NULL

--4. Find all departments whose total salary is exceeding 100000.
SELECT D.DEPARTMENTNAME,SUM(P.SALARY) FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY D.DEPARTMENTNAME
HAVING SUM(P.SALARY) > 10000

