----------------PART-A----------------
--1. Find all persons with their department name & code.
SELECT P.PERSONNAME,D.DEPARTMENTNAME,D.DEPARTMENTID FROM PERSON AS P
LEFT JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID

--2. Find the person's name whose department is in C-Block.
SELECT P.PERSONNAME FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE D.LOCATION = 'C-BLOCK'

--3. Retrieve person name, salary & department name who belongs to Jamnagar city.
SELECT P.PERSONNAME,P.SALARY,D.DEPARTMENTNAME FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID

--4. Retrieve person name, salary & department name who does not belong to Rajkot city.
SELECT P.PERSONNAME,P.SALARY,D.DEPARTMENTNAME FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE P.CITY <> 'RAJKOT'

--5. Retrieve person’s name of the person who joined the Civil department after 1-Aug-2001.
SELECT P.PERSONNAME,P.JOININGDATE FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE P.JOININGDATE > '2001-8-1' AND D.DEPARTMENTNAME = 'CIVIL'

--6. Find details of all persons who belong to the computer department.
SELECT P.PERSONNAME,D.DEPARTMENTNAME FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE D.DEPARTMENTNAME ='COMPUTER'

--7. Display all the person's name with the department whose joining date difference with the current date 
--is more than 365 days.
SELECT P.PERSONNAME,D.DEPARTMENTNAME FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
WHERE DATEDIFF(DAY,P.JOININGDATE,GETDATE()) > 365

--8. Find department wise person counts.
SELECT D.DEPARTMENTNAME,COUNT(P.PERSONID) FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY D.DEPARTMENTNAME

--9. Give department wise maximum & minimum salary with department name.
SELECT D.DEPARTMENTNAME,MAX(P.SALARY),MIN(P.SALARY),D.DEPARTMENTNAME FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY D.DEPARTMENTNAME

--10. Find city wise total, average, maximum and minimum salary.
SELECT P.CITY,SUM(P.SALARY),AVG(P.SALARY),MAX(P.SALARY),MIN(P.SALARY) FROM PERSON AS P
JOIN DEPT AS D
ON P.DEPARTMENTID = D.DEPARTMENTID
GROUP BY P.CITY

--11. Find the average salary of a person who belongs to Ahmedabad city.
SELECT P.CITY,AVG(P.SALARY) FROM PERSON AS P
GROUP BY P.CITY
HAVING P.CITY = 'AHMEDABAD'

--12. Produce Output Like: <PersonName> lives in <City> and works in <DepartmentName> Department. (In 
--single colum
SELECT P.PERSONNAME+'LIVES IN'+P.CITY+'AND WORKS IN'+D.DEPARTMENTNAME FROM PERSON AS P
LEFT JOIN DEPT D
ON P.DEPARTMENTID=D.DEPARTMENTID

